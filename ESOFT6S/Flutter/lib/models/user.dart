class User {
  String nome;
  String email;
  String cpf;
  String telefone;
  String rg;
  double salario;
  String cep;
  String cidadeEstado;

  User({
    required this.nome,
    required this.email,
    required this.cpf,
    required this.telefone,
    required this.rg,
    required this.salario,
    required this.cep,
    required this.cidadeEstado,
  });
}
